int	ft_iterative_factorial(int nb)
{	
	int	i;
	int	x;

	i = 1;
	x = 1;
	if (nb < 0 || nb > 15)
		return (0);
	if (nb == 0)
		return (1);
	while (i <= nb)
	{
		x = x * i;
		i++;
	}
	return (x);
}
